public class Calculator {

    public static int add(int no1, int no2) {
        return no1 + no2;
    }

    public static int substract(int no1, int no2) {
        return no1 - no2;
    }

    public static int devide(int no1, int no2) {
        return no1 / no2;
    }

    public static int multiply(int no1, int no2) {
        return no1 * no2;
    }
}
